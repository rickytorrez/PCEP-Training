# not operator -> gives opposite of what you're working with
print(not True) #false
print(not False) #true

# or operator -> needs one value to return true
print(True or True)
print(True or False)
print(False or False)
print(False or True)

# and operator -> requires both operands to be true
print(True and True)
print(True and False)
print(False and False)
print(False and True)

