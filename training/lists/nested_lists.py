# nested lists
# matrices

my_matrix = [[1,2,3,],
             [4,5,6 ]]

print(my_matrix) # [[1, 2, 3], [4, 5, 6]]

# row count
row_count = len(my_matrix)
print(row_count)

# column count
column_count = len(my_matrix[0])
print(column_count)

# interact with a single item
print(my_matrix[0][1]) # 2
print(my_matrix[1][2]) # 6