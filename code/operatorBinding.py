# order of operators in python
# (), [], attributes, function, operators, exponents, positive operator
# negative operator, bitwise compliment, mult-div-modulo, add-subs,
# bitwise(shift, and, or)
print(14 & 3 * 2 + 4) #10

print(14 & 3 * (2 + 4)) #2

print((14 & 3) * 2 + 4) #8

print(14 & (3 * 2) + 4) #10
